#include "push_swap.h"

void		populate_b(t_lst *a, t_lst *b)
{
	int		pb_sz;

	pb_sz = ft_lstsz(a) / 2;
	while (pb_sz > 0)
	{
		op_pb(&a, &b);
		pb_sz--;
	}
}
