#include "libft.h"

t_lst	*ft_lstmin2(t_lst *nod1, t_lst *nod2, int (*cmp)(t_lst *a, t_lst *b))
{
	return (cmp(nod1, nod2) < 0 ? nod1 : nod2);
}
